# COMMAND ----------

# MAGIC %sql
# MAGIC CREATE TABLE IF NOT EXISTS gold.dim_data (
# MAGIC   SK_DATA     BIGINT GENERATED BY DEFAULT AS IDENTITY,
# MAGIC   DT_COMPLETA DATE,
# MAGIC   DIA         INT,
# MAGIC   MES         INT,
# MAGIC   ANO         INT,
# MAGIC   TRIMESTRE   INT,
# MAGIC   DIA_SEMANA  STRING,
# MAGIC   NOME_MES    STRING
# MAGIC ) USING DELTA;

# COMMAND ----------

# MAGIC %sql
# MAGIC DELETE FROM gold.dim_data;

# COMMAND ----------

df_dim_data = spark.read.format("delta").table("silver.pedidos") \
    .select(to_date(col("DT_COMPRA")).alias("DT_COMPLETA")) \
    .dropDuplicates(["DT_COMPLETA"]) \
    .dropna(subset=["DT_COMPLETA"]) \
    .withColumn("DIA",       dayofmonth(col("DT_COMPLETA"))) \
    .withColumn("MES",       month(col("DT_COMPLETA"))) \
    .withColumn("ANO",       year(col("DT_COMPLETA"))) \
    .withColumn("TRIMESTRE", quarter(col("DT_COMPLETA"))) \
    .withColumn("DIA_SEMANA", date_format(col("DT_COMPLETA"), "EEEE")) \
    .withColumn("NOME_MES",   date_format(col("DT_COMPLETA"), "MMMM"))

df_dim_data.write.format("delta").mode("append").saveAsTable("gold.dim_data")
print(f"gold.dim_data - {df_dim_data.count():,} registros")

