# MAGIC %md
# MAGIC ### 5. fato_pedido

# COMMAND ----------

# MAGIC %sql
# MAGIC CREATE TABLE IF NOT EXISTS gold.fato_pedido (
# MAGIC   SK_FATO_PEDIDO  BIGINT GENERATED BY DEFAULT AS IDENTITY,
# MAGIC   ID_PEDIDO       STRING,
# MAGIC   ID_CLIENTE      STRING,
# MAGIC   ID_PRODUTO      STRING,
# MAGIC   ID_VENDEDOR     STRING,
# MAGIC   DT_COMPRA       DATE,
# MAGIC   STATUS_PEDIDO   STRING,
# MAGIC   VALOR_ITEM      DOUBLE,
# MAGIC   VALOR_FRETE     DOUBLE,
# MAGIC   VALOR_PAGAMENTO DOUBLE,
# MAGIC   NUM_PARCELAS    INT,
# MAGIC   TIPO_PAGAMENTO  STRING,
# MAGIC   NOTA_AVALIACAO  INT
# MAGIC ) USING DELTA;

# COMMAND ----------

# MAGIC %sql
# MAGIC DELETE FROM gold.fato_pedido;

# COMMAND ----------

df_pedidos    = spark.read.format("delta").table("silver.pedidos")
df_itens      = spark.read.format("delta").table("silver.itens_pedido")
df_pagamentos = spark.read.format("delta").table("silver.pagamentos")
df_avaliacoes = spark.read.format("delta").table("silver.avaliacoes")

df_pag_agg = df_pagamentos \
    .groupBy("ID_PEDIDO", "TIPO_PAGAMENTO") \
    .agg(
        spark_sum("VALOR_PAGAMENTO").alias("VALOR_PAGAMENTO"),
        spark_sum("NUM_PARCELAS").alias("NUM_PARCELAS")
    ) \
    .dropDuplicates(["ID_PEDIDO"])

df_aval_agg = df_avaliacoes \
    .select("ID_PEDIDO", "NOTA") \
    .dropDuplicates(["ID_PEDIDO"])

df_fato = df_pedidos.alias("p") \
    .join(df_itens.alias("i"),     "ID_PEDIDO", "inner") \
    .join(df_pag_agg.alias("pg"),  "ID_PEDIDO", "left") \
    .join(df_aval_agg.alias("av"), "ID_PEDIDO", "left") \
    .select(
        col("p.ID_PEDIDO"),
        col("p.ID_CLIENTE"),
        col("i.ID_PRODUTO"),
        col("i.ID_VENDEDOR"),
        to_date(col("p.DT_COMPRA")).alias("DT_COMPRA"),
        col("p.STATUS_PEDIDO"),
        col("i.VALOR_ITEM"),
        col("i.VALOR_FRETE"),
        col("pg.VALOR_PAGAMENTO"),
        col("pg.NUM_PARCELAS"),
        col("pg.TIPO_PAGAMENTO"),
        col("av.NOTA").alias("NOTA_AVALIACAO")
    )

df_fato.write \
    .format("delta") \
    .mode("overwrite") \
    .option("overwriteSchema", "true") \
    .saveAsTable("gold.fato_pedido")

print(f"gold.fato_pedido - {df_fato.count():,} registros")

